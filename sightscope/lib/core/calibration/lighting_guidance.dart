/// Lighting guidance copy shown before a test. Educational, non-clinical.
class LightingGuidance {
  const LightingGuidance._();

  static const String headline = 'Set up your lighting';

  static const String body =
      'Use soft, even lighting in the room. Avoid glare, direct reflections on '
